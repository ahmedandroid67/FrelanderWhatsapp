const getSystemLanguage = () => {
  const locales = Localization.getLocales();
  if (locales && locales.length > 0) {
    const langCode = locales[0].languageCode;
    if (langCode && Object.keys(resources).includes(langCode)) {
      return langCode;
    }
  }
  return 'en';
};

export const initI18n = async () => {
  let savedLanguage = await AsyncStorage.getItem(ASYNC_STORAGE_KEY);
  let initialLang = savedLanguage || getSystemLanguage();

  await i18n.use(initReactI18next).init({
    resources,
    lng: initialLang,
    fallbackLng: 'en',
    interpolation: {
      escapeValue: false,
    },
  });

  const isRTL = i18n.dir(initialLang) === 'rtl';
  if (I18nManager.isRTL !== isRTL) {
    I18nManager.allowRTL(isRTL);
    I18nManager.forceRTL(isRTL);
  }

  return i18n;
};

export const changeLanguage = async (lng: string) => {
  await AsyncStorage.setItem(ASYNC_STORAGE_KEY, lng);
  await i18n.changeLanguage(lng);
  
  const isRTL = i18n.dir(lng) === 'rtl';
  if (I18nManager.isRTL !== isRTL) {
    I18nManager.allowRTL(isRTL);
    I18nManager.forceRTL(isRTL);
  }
};

export default i18n;
